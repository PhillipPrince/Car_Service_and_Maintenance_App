package com.example.carservice;

public class mechanicsList {
    String mechName;
    String mechLocation;
    String mechPhone;

    public String getMechName() {
        return mechName;
    }

    public void setMechName(String mechName) {
        this.mechName = mechName;
    }

    public String getMechLocation() {
        return mechLocation;
    }

    public void setMechLocation(String mechLocation) {
        this.mechLocation = mechLocation;
    }

    public String getMechPhone() {
        return mechPhone;
    }

    public void setMechPhone(String mechPhone) {
        this.mechPhone = mechPhone;
    }
}
